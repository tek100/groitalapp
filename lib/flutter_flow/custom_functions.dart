String? greetingFunction(DateTime currentTime) {
  // a greeting function
  String greeting = '';
  int hour = currentTime.hour;

  if (hour < 12) {
    greeting = 'Good morning';
  } else if (hour < 17) {
    greeting = 'Good afternoon';
  } else {
    greeting = 'Good evening';
  }

  return greeting;
}
